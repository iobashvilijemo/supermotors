const translatableNodes = document.querySelectorAll("[data-i18n]");
const languageButtons = document.querySelectorAll(".lang-btn");
const yearNode = document.querySelector("#year");
const heroVideo = document.querySelector("#hero-video");
const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  document.documentElement.lang = language;

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("supermotors-language", language);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

yearNode.textContent = new Date().getFullYear();
setLanguage(localStorage.getItem("supermotors-language") || "en");

function setupScrollReveals() {
  const revealGroups = [
    { selector: ".hero-copy", variant: "reveal-left", immediate: true },
    { selector: ".hero-card", variant: "reveal-right", immediate: true },
    { selector: ".section-heading", variant: "reveal-left" },
    { selector: ".service-card", variant: "reveal-scale", stagger: true },
    { selector: ".popular-parts", variant: "reveal-scale" },
    { selector: ".order-step", variant: "reveal-left", stagger: true },
    { selector: ".request-panel", variant: "reveal-right" },
    { selector: ".experience-media", variant: "reveal-left" },
    { selector: ".experience-copy", variant: "reveal-right" },
    { selector: ".experience-stats span", variant: "reveal-scale", stagger: true },
    { selector: ".contact-row", variant: "reveal-left", stagger: true },
    { selector: ".map-panel", variant: "reveal-right" }
  ];

  const revealItems = revealGroups.flatMap((group) =>
    Array.from(document.querySelectorAll(group.selector)).map((node, index) => ({
      node,
      index,
      group
    }))
  );

  revealItems.forEach(({ node, index, group }) => {
    node.classList.add("reveal-on-scroll", group.variant);

    if (group.stagger) {
      node.style.setProperty("--reveal-delay", `${Math.min(index * 80, 420)}ms`);
    }

    if (group.immediate) {
      node.classList.add("is-visible");
    }
  });

  if (prefersReducedMotion.matches || !("IntersectionObserver" in window)) {
    revealItems.forEach(({ node }) => node.classList.add("is-visible"));
    return;
  }

  if (window.location.hash) {
    revealItems.forEach(({ node }) => node.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      rootMargin: "0px 0px -12% 0px",
      threshold: 0.16
    }
  );

  revealItems
    .filter(({ group }) => !group.immediate)
    .forEach(({ node }) => observer.observe(node));

  const revealVisibleItems = () => {
    revealItems
      .filter(({ group }) => !group.immediate)
      .forEach(({ node }) => {
        const bounds = node.getBoundingClientRect();
        const isInView = bounds.top < window.innerHeight * 0.9 && bounds.bottom > 0;

        if (isInView) {
          node.classList.add("is-visible");
          observer.unobserve(node);
        }
      });
  };

  requestAnimationFrame(revealVisibleItems);
  window.setTimeout(revealVisibleItems, 300);
  window.setTimeout(revealVisibleItems, 1000);
  window.addEventListener("load", revealVisibleItems);
  window.addEventListener("scroll", revealVisibleItems, { passive: true });
  window.addEventListener("hashchange", () => window.setTimeout(revealVisibleItems, 50));
}

setupScrollReveals();

if (heroVideo) {
  const videoSource = heroVideo.dataset.videoSrc;
  let heroVideoStarted = false;

  const revealHeroVideo = () => {
    if (heroVideo.readyState >= 2) {
      heroVideo.classList.add("is-ready");
    }
  };

  const requestHeroPlayback = () => {
    if (heroVideoStarted) {
      return;
    }

    heroVideo.muted = true;
    heroVideo.defaultMuted = true;
    heroVideo.playsInline = true;
    heroVideo.autoplay = true;

    const playRequest = heroVideo.play();

    if (playRequest?.then) {
      playRequest
        .then(() => {
          heroVideoStarted = true;
          revealHeroVideo();
        })
        .catch(() => {
          heroVideoStarted = false;
          heroVideo.classList.remove("is-ready");
        });
    } else {
      heroVideoStarted = true;
      revealHeroVideo();
    }
  };

  heroVideo.addEventListener("loadeddata", revealHeroVideo);
  heroVideo.addEventListener("canplay", revealHeroVideo);
  heroVideo.addEventListener("playing", () => {
    heroVideoStarted = true;
    revealHeroVideo();
  });

  document.addEventListener("visibilitychange", () => {
    if (!document.hidden) {
      requestHeroPlayback();
    }
  });

  if (heroVideo.canPlayType("application/vnd.apple.mpegurl")) {
    heroVideo.src = videoSource;
    heroVideo.load();
    requestHeroPlayback();
  } else if (window.Hls?.isSupported()) {
    const hls = new Hls({
      abrEwmaDefaultEstimate: 12000000,
      autoStartLoad: false,
      capLevelToPlayerSize: false,
      testBandwidth: false
    });
    let preferredLevel = -1;

    hls.on(Hls.Events.MANIFEST_PARSED, () => {
      preferredLevel = hls.levels.reduce((bestIndex, level, index) => {
        if (bestIndex === -1 || level.bitrate > hls.levels[bestIndex].bitrate) {
          return index;
        }

        return bestIndex;
      }, -1);

      if (preferredLevel >= 0) {
        hls.startLevel = preferredLevel;
        hls.currentLevel = preferredLevel;
        hls.loadLevel = preferredLevel;
        hls.nextLevel = preferredLevel;
      }

      hls.startLoad(0);
    });

    hls.on(Hls.Events.FRAG_BUFFERED, (_event, data) => {
      if (data.frag?.type === "main" && data.frag.level === preferredLevel) {
        heroVideo.currentTime = 0;
        requestHeroPlayback();
      }
    });

    hls.on(Hls.Events.ERROR, (_event, data) => {
      if (data.fatal) {
        hls.destroy();
        heroVideo.classList.remove("is-ready");
        heroVideo.removeAttribute("src");
      }
    });

    hls.loadSource(videoSource);
    hls.attachMedia(heroVideo);
  }
}
